/*
#include <cstdlib>는
C++ 프로그래밍 언어에서 제공하는 표준 라이브러리 중 하나인 "cstdlib" 라이브러리를 포함하는 헤더 파일입니다.

이 헤더 파일은 다양한 종류의 >> 유틸리티 함수와 매크로를 제공 <<합니다.
예를 들어, 다음과 같은 함수와 매크로가 있습니다.

exit() : 프로그램을 종료합니다.
atoi() : 문자열을 정수로 변환합니다.
atof() : 문자열을 실수로 변환합니다.
rand() : 무작위 수를 생성합니다.
srand() : rand() 함수에서 사용할 시드 값을 설정합니다.
NULL : 포인터를 나타내는 매크로입니다.
이러한 함수와 매크로는 프로그램에서 자주 사용되며, 특히 >>수치 계산과 프로그램의 종료와 같은 상황에서 유용<<합니다.
*/